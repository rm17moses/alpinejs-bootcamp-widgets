function greet(name, language) {
    if (language === 'English') {
      return 'Hello, ' + name;
    } else if (language === 'SeSotho') {
      return 'Dumela, ' + name;
    } else if (language === 'Spanish') {
      return 'Hola, ' + name;
    } else if (language === 'IsiZulu') {
      return 'Sawubona, ' + name;
    } else if (language === 'French') {
      return 'Bonjour, ' + name;
    } else {
      return 'Hello, ' + name;
    }
  }
  